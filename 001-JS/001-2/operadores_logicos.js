// Operadores Lógicos

console.log('=========== Operadores lógicos ===========')

var numero1 = 5;
var numero2 = 6;

console.log(`O ${numero1} é maior(>) que ${numero2} ? ${numero1 > numero2}`)

console.log(`O ${numero1} é menor(<) que ${numero2} ? ${numero1 < numero2}`)

console.log(`O ${numero1} é maior ou igual(>=) a ${numero2} ? ${numero1 >= numero2}`)

console.log(`O ${numero1} é menor ou igual(<=) a ${numero2} ? ${numero1 <= numero2}`)

// == compara apenas o valor
console.log(`O ${numero1} é igual(==) a ${"5"} ? ${numero1 == "5"}`)
// === compara o valor e o tipo
console.log(`O ${numero1} é igual(===) a ${"5"} ? ${numero1 === "5"}`) 

console.log(`O ${numero1} é diferente(!=) a ${numero2} ? ${numero1 != numero2}`)