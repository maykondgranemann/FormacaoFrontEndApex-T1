var dia = "sabado";

switch(dia){
    case "segunda":
        console.log("Segunda-Feira");
        break;
    case "terca":
        console.log("Terça-Feira");
        break;
    case "quarta":
        console.log("Quarta-Feira");
        break;
    case "quinta":
        console.log("Quinta-Feira");
        break;
    case "sexta":
        console.log("Sexta-Feira");
        break;
    case "sabado":
        console.log("Sábado");
        break;
    case "domingo":
        console.log("Domingo");
        break;
    default:
        console.log("Dia inválido!");
}